let Store_Locator = document.querySelector(".store-locator-container")
let searchBox = document.getElementById("searchBox");


function handleSearch() {
  const searchTerm = searchBox.value.toLowerCase();
  const filteredProducts = store.filter(store => store.name.toLowerCase().includes(searchTerm));
  renderProducts(filteredProducts);
}

function renderProducts(store) {
 Store_Locator.innerHTML = ""; 


for (let index = 0; index < store.length; index++) {
  Store_Locator.innerHTML += `<div class="store-locator-card">
    <h2>${store[index].name}</h2>
   <span id="store-span">${store[index].num}</span>
   <div id="open-close">
    <p>${store[index].opening}</p>
   <p>${store[index].close}</p>
</div>
   <p>${store[index].distance}</p>
   <div class="direction-btn">
    <button onclick='handleLocation(${index})'><span>View on Map</span></button>
   </div>
</div>`
}
}
renderProducts(store);

